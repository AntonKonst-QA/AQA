package Homework.Work_3;

public class MathConstants {
    final double PI = 3.14159;
    final double E = 2.71828;

    //Метод
    static double calculateCircleArea(double r) {
        return Math.PI * r * r;
    }

    static double calculateCircumference(double r) {
        return 2 * Math.PI * r;
    }


}
